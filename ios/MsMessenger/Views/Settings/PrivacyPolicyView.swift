import SwiftUI

struct PrivacyPolicyView: View {
    @ObservedObject private var theme = ThemeManager.shared

    var body: some View {
        ScrollView {
            VStack(alignment: .leading, spacing: 24) {
                section("1. Общие положения", text: """
                MS Messenger — мессенджер для обмена сообщениями между пользователями. Мы стремимся обеспечить безопасность данных и обрабатываем только ту информацию, которая необходима для работы приложения.

                Используя MS Messenger, вы соглашаетесь с настоящей Политикой конфиденциальности.
                """)

                section("2. Какие данные мы собираем", text: """
                Для работы приложения могут обрабатываться следующие данные:

                • Имя пользователя
                • Уникальный ID (@username)
                • Хеш пароля (bcrypt)
                • Зашифрованные сообщения
                • ID устройства
                • Время отправки сообщений
                • Дата регистрации
                • Время последнего входа
                • Настройки пользователя

                MS Messenger НЕ собирает:
                • номер телефона
                • адрес электронной почты
                • геолокацию
                • список контактов
                • рекламные идентификаторы
                """)

                section("3. Безопасность данных", text: """
                • Пароли хранятся в виде хеша (bcrypt)
                • Сообщения шифруются AES-256-GCM
                • Передача по HTTPS/WSS
                • JWT-токены для авторизации
                • Подтверждение при входе с нового устройства
                """)

                section("4. Сообщения", text: """
                Сообщения используются исключительно для доставки адресату. MS Messenger не использует содержимое переписки для рекламы, аналитики или продажи данных.
                """)

                section("5. Системный аккаунт", text: """
                Официальный системный аккаунт: MS Messenger (@ms-messenger)

                Используется для:
                • кодов подтверждения
                • уведомлений безопасности
                • приветственного сообщения
                """)

                section("6. Хранение данных", text: """
                • Данные хранятся только на сервере
                • Сообщения сохраняются до удаления пользователем
                • После удаления сообщение перестаёт отображаться
                """)

                section("7. Аккаунты", text: """
                • На одном устройстве — не более двух аккаунтов
                • Сессии сохраняются локально
                • Пользователь отвечает за сохранность устройства и пароля
                """)

                section("8. Ваши права", text: """
                Вы можете:
                • удалить аккаунт
                • удалить свои сообщения
                • изменить имя
                • выйти со всех устройств
                """)

                section("9. Передача данных", text: """
                MS Messenger не продаёт данные, не передаёт их рекламным компаниям, не предоставляет доступ третьим лицам, кроме случаев, предусмотренных законом.
                """)

                section("10. Обязанности пользователя", text: """
                • используйте надёжный пароль
                • не передавайте коды подтверждения
                • сообщайте о подозрительной активности
                """)

                section("11. Изменение политики", text: """
                Мы можем обновлять Политику. При существенных изменениях пользователи будут уведомлены через приложение.
                """)

                section("12. Контакты", text: """
                По вопросам конфиденциальности обращайтесь к администратору сервера MS Messenger.
                """)
            }
            .padding(.horizontal, 20)
            .padding(.vertical, 16)
        }
        .background(theme.bgColor.ignoresSafeArea())
        .navigationTitle("Конфиденциальность")
        .navigationBarTitleDisplayMode(.inline)
        .toolbar {
            ToolbarItem(placement: .principal) {
                Text("Конфиденциальность")
                    .font(.system(size: 17, weight: .semibold))
                    .foregroundColor(.white)
            }
        }
        .toolbarBackground(Color.clear, for: .navigationBar)
        .toolbarBackground(.visible, for: .navigationBar)
    }

    private func section(_ title: String, text: String) -> some View {
        VStack(alignment: .leading, spacing: 8) {
            Text(title)
                .font(.system(size: 17, weight: .semibold))
                .foregroundColor(Color(hex: "#6C63FF"))
            Text(text)
                .font(.system(size: 15))
                .foregroundColor(theme.textSecondary)
                .lineSpacing(4)
        }
    }
}
