import SwiftUI

struct PrivacyPolicyView: View {
    @ObservedObject private var theme = ThemeManager.shared

    var body: some View {
        ScrollView {
            VStack(alignment: .leading, spacing: 20) {
                Text("Политика конфиденциальности MS Messenger")
                    .font(.title).bold()
                    .foregroundColor(theme.textPrimary)
                Text("Дата вступления в силу: 13 июля 2026 г.")
                    .font(.subheadline)
                    .foregroundColor(theme.textSecondary)

                section("1. Общие положения") {
                    """
                    MS Messenger — мессенджер для обмена сообщениями между пользователями. Мы стремимся обеспечить безопасность данных и обрабатываем только ту информацию, которая необходима для работы приложения.

                    Используя MS Messenger, вы соглашаетесь с настоящей Политикой конфиденциальности.
                    """
                }

                section("2. Какие данные мы собираем") {
                    """
                    Для работы приложения могут обрабатываться следующие данные:

                    • Имя пользователя — Отображение профиля и сообщений
                    • Уникальный ID (@username) — Поиск пользователей и идентификация аккаунта
                    • Хеш пароля (bcrypt) — Безопасная аутентификация
                    • Зашифрованные сообщения — Передача и хранение переписки
                    • ID устройства — Проверка новых входов и повышение безопасности
                    • Время отправки сообщений — Работа чатов и сортировка сообщений
                    • Дата регистрации — Создание аккаунта
                    • Время последнего входа — Защита аккаунта и отображение активности
                    • Настройки пользователя — Сохранение выбранных параметров приложения

                    MS Messenger НЕ собирает:
                    • номер телефона;
                    • адрес электронной почты;
                    • геолокацию;
                    • список контактов;
                    • фотографии без вашего действия;
                    • микрофон и камеру без вашего разрешения;
                    • рекламные идентификаторы;
                    • данные для персонализированной рекламы.
                    """
                }

                section("3. Безопасность данных") {
                    """
                    Для защиты данных используются современные методы безопасности.

                    • Пароли хранятся только в виде хеша с использованием bcrypt.
                    • Сообщения шифруются алгоритмом AES-256-GCM перед сохранением.
                    • Передача данных осуществляется по защищённому соединению HTTPS/WSS.
                    • Для авторизации используются JWT-токены.
                    • При входе с нового устройства требуется подтверждение.
                    • Коды подтверждения действуют ограниченное время и используются только один раз.
                    """
                }

                section("4. Сообщения") {
                    """
                    Сообщения используются исключительно для их доставки адресату.

                    MS Messenger не использует содержимое переписки для рекламы, аналитики или продажи данных.
                    """
                }

                section("5. Системный аккаунт") {
                    """
                    В приложении существует только один официальный системный аккаунт:

                    MS Messenger (@ms-messenger)

                    Он используется только для:
                    • отправки кодов подтверждения;
                    • уведомлений безопасности;
                    • приветственного сообщения;
                    • важных уведомлений о работе приложения.
                    """
                }

                section("6. Хранение данных") {
                    """
                    • Данные хранятся только на сервере MS Messenger.
                    • Сообщения сохраняются до удаления пользователем.
                    • После удаления сообщение перестаёт отображаться в чатах.
                    • Резервные копии могут временно содержать удалённые данные до их автоматического обновления.
                    """
                }

                section("7. Аккаунты") {
                    """
                    • На одном устройстве допускается создание не более двух аккаунтов.
                    • Сессии сохраняются локально на устройстве пользователя.
                    • Пользователь несёт ответственность за сохранность своего устройства и пароля.
                    """
                }

                section("8. Ваши права") {
                    """
                    Вы можете:
                    • удалить аккаунт;
                    • удалить свои сообщения;
                    • изменить имя пользователя;
                    • запросить информацию о данных, связанных с вашим аккаунтом;
                    • выйти со всех устройств.
                    """
                }

                section("9. Передача данных") {
                    """
                    MS Messenger:
                    • не продаёт персональные данные;
                    • не передаёт их рекламным компаниям;
                    • не использует данные для показа рекламы;
                    • не предоставляет доступ к сообщениям третьим лицам, кроме случаев, предусмотренных законодательством.
                    """
                }

                section("10. Обязанности пользователя") {
                    """
                    Пользователь обязан:
                    • использовать надёжный пароль;
                    • не передавать коды подтверждения другим лицам;
                    • своевременно сообщать о подозрительной активности;
                    • соблюдать правила использования сервиса.
                    """
                }

                section("11. Изменение политики") {
                    """
                    Мы можем обновлять настоящую Политику конфиденциальности. При существенных изменениях пользователи будут уведомлены через приложение.

                    Новая редакция вступает в силу с момента её публикации.
                    """
                }

                section("12. Контакты") {
                    """
                    По вопросам конфиденциальности, безопасности или обработки данных обращайтесь к администратору сервера MS Messenger.
                    """
                }
            }
            .padding()
        }
        .background(theme.backgroundColor)
        .navigationTitle("Политика конфиденциальности")
        .navigationBarTitleDisplayMode(.inline)
        .toolbarBackground(.ultraThinMaterial, for: .navigationBar)
        .toolbarBackground(.visible, for: .navigationBar)
    }

    private func section(_ title: String, text: () -> String) -> some View {
        VStack(alignment: .leading, spacing: 8) {
            Text(title)
                .font(.headline).bold()
                .foregroundColor(theme.accent)
            Text(text())
                .font(.subheadline)
                .foregroundColor(theme.textPrimary)
                .lineSpacing(4)
        }
    }
}
